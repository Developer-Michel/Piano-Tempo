export function t(key: string, lang: Language): string {
  const keys = key.split(".");
  let value: any = translations;
  for (const k of keys) {
    value = value?.[k];
  }
  if (typeof value === "object" && value !== null && lang in value) {
    return value[lang];
  }
  return key;
}
